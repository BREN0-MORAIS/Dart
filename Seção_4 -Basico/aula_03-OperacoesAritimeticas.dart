void main() {
  double n1 = 5.1; //declarando a variavel do tipo double
  double n2 = 5.6; //declarando a variavel do tipo double

  double result = n1 + n2; //Realizando o calculo do n1 e n2
  n1 += 15; //somando o n1 mais 15
  n1 *= 15; //multiplicando o n1 veze 15
  n1 -= 15; //subtraindo o n1 menos 15;
  n1 /= 15; //dividindo n1 dividio por 15

  print(result);
  print(n1);
}
